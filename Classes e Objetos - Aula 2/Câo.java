public class Câo {


    String nome;
    String raça;
    int idade;
    String porte;
    String tipoPelo;
    String sexo;
    Boolean vacina;


    public Câo() {
    }

    public Câo(String nome, String raça, int idade, String porte, String tipoPelo, String sexo, Boolean vacina ) {

        this.nome = nome;
        this.raça = raça;
        this.idade = idade;
        this.porte = porte;
        this.tipoPelo = tipoPelo;
        this.sexo = sexo;
        this.vacina = vacina;
    }

}
